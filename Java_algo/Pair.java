
public class Pair {
	int first;
	int second;
	
	public Pair(int p1,int p2){
		first=p1;
		second=p2;
	}

	public int getFirst() {
		return first;
	}

	public void setFirst(int first) {
		this.first = first;
	}

	public int getSecond() {
		return second;
	}

	public void setSecond(int second) {
		this.second = second;
	}

}
